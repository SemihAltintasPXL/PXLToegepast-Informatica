# Definieer drie variabelen var1, var2 en var3. Vraag de gebruiker voor input van 3 gehele getallen voor de 3 variabelen.
# Bereken het gemiddelde en stop het in een variabele gemiddelde. Toon het gemiddelde.


var1 = int(input("geef een eerste getal in "))
var2 = int(input("geef een tweede getal in "))
var3 = int(input("geef een derde getal in "))

# berekenen van de gemiddelde van deze 3 getallen
gemiddelde = (var1 + var2 + var3) / 3
print(gemiddelde)
